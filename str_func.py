def str_upper():
    """
    принимает на вход строку и возвращает ее со всеми заглавными буквами
    Добавляю что-то ещё, чтобы получить конфликт
    """
    return input().upper()

def str_title():
    """
    принимает на вход строку и возвращает ее с первой заглавной буквой
    """
    return input().title()
